# Arithmetic operator

x=7
y=3
print(x+y)  #Addition
print(x-y)  #Subtraction
print(x*y)  #Multiplication
print(x/y)  #Division
print(x%y)  #Remainder

# Comparison operators
a=12
b=34

print(f"{a} is equal to {b} {a==b}")
print(f"{a} is not equal to {b} {a!=b}")
print(f"{a} is greater than {b} {a>b}")
print(f"{a} is greater than or equals to {b} {a>=b}")
print(f"{a} is less than  {b} {a<b}")
print(f"{a} is less than or equals to {b} {a<=b}")

#Logical Operator
c=5
print(f"is this statement true or false? {c>10 and c>3}")
print(f"is this statement true or false? {c>10 or c>3}")

